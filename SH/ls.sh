#!/bin/bash
# -------------------------------------------------------------------
# ls.sh
# -------------------------------------------------------------------

#:begin
echo "---------------------------------"
echo "ls [опции] /путь/к/папке"
echo "---------------------------------"
echo "[опции]"
echo "---------------------------------"
echo "-a - отображать все файлы, включая скрытые, это те, перед именем которых стоит точка"
echo "-A - не отображать ссылку на текущую папку и корневую папку . и .."
echo "--author - выводить создателя файла в режиме подробного списка"
echo "-b - выводить Escape последовательности вместо непечатаемых символов"
echo "--block-size - выводить размер каталога или файла в определенной единице измерения, например, мегабайтах, гигабайтах или килобайтах"
echo "-B - не выводить резервные копии, их имена начинаются с ~"
echo "-c - сортировать файлы по времени модификации или создания, сначала будут выведены новые файлы"
echo "-C - выводить колонками"
echo "--color - включить цветной режим вывода, автоматически активирована во многих дистрибутивах"
echo "-d - выводить только директории, без их содержимого, полезно при рекурсивном выводе"
echo "-D - использовать режим вывода, совместимый с Emacs"
echo "-f - не сортировать"
echo "-F - показывать тип объекта, к каждому объекту будет добавлен один из специализированных символов */=>@|"
echo "--full-time - показывать подробную информацию, плюс вся информация о времени в формате ISO"
echo "-g - показывать подробную информацию, но кроме владельца файла"
echo "--group-directories-first - сначала отображать директории, а уже потом файлы"
echo "-G - не выводить имена групп"
echo "-h - выводить размеры папок в удобном для чтения формате"
echo "-H - открывать символические ссылки при рекурсивном использовании"
echo "--hide - не отображать файлы, которые начинаются с указанного символа"
echo "-i - отображать номер индекса inode, в которой хранится этот файл"
echo "-l - выводить подробный список, в котором будет отображаться владелец, группа, дата создания, размер и другие параметры"
echo "-L - для символических ссылок отображать информацию о файле, на который они ссылаются"
echo "-m - разделять элементы списка запятой"
echo "-n - выводить UID и GID вместо имени и группы пользователя"
echo "-N - выводить имена как есть, не обрабатывать контролирующие последовательности"
echo "-Q - брать имена папок и файлов в кавычки"
echo "-r - обратный порядок сортировки"
echo "-R - рекурсивно отображать содержимое поддиректорий"
echo "-s - выводить размер файла в блоках"
echo "-S - сортировать по размеру, сначала большие"
echo "-t - сортировать по времени последней модификации"
echo "-u - сортировать по времени последнего доступа"
echo "-U - не сортировать"
echo "-X - сортировать по алфавиту"
echo "-Z - отображать информацию о расширениях SELinux"
echo "-1 - отображать один файл на одну строку"
echo "---------------------------------"
echo "[примеры]"
echo "---------------------------------"
echo "содержимое текущей папки"
echo "ls"
echo "содержимое папки в виде списка с максимальным количеством информации"
echo "ls -l"
echo "Колонка создателя"
echo "ls -l --author"
echo "Колонка размер"
echo "ls -lh"
echo "скрытые файлы"
echo "ls -a"
echo "скрытые файлы без ссылок на текущую и родительскую папку"
echo "ls -A"
echo "отсортируем файлы по размеру"
echo "ls -lSh"
echo "отсортируем файлы по размеру обратный порядок"
echo "ls -lShr"
echo "отсортируем по алфавиту"
echo "ls -lX"
echo "сортируем по времени последней модификации"
echo "ls -lt"
echo "какие метки SELinux присвоены файлу"
echo "ls -lZ"
echo "список папок и файлов в директории через запятую"
echo "ls -m"
echo "---------------------------------"

#:Exit
